function overlapCardsAndCreateClickableDivs() {
  let windowWidth = window.innerWidth * 0.8;
  let numCards = $('.game-card').length;
  if (numCards === 0) {
    return;
  }
  let cardWidth = (windowWidth - 180) / (numCards - 1);
  let numMarginsChanged = 0;
  $('.game-card').each(function() {
    $(this).css('margin-right', '-' + (180 - cardWidth) + 'px');
    numMarginsChanged++;
    if (numMarginsChanged == numCards - 1) {
      return false;
    }
  });
  $('.clickable-game-card').remove();
  let overlappedClickableCardWidth = 0;
  if (numCards > 1) {
    let left = -1;
    $('.game-card-img').each(function() {
      let thisLeft = $(this).offset().left;
      if (left < 0) {
        left = thisLeft;
      } else {
        overlappedClickableCardWidth = thisLeft - left;
        return false;
      }
    });
  }
  let numClickableDivsCreated = 0;
  $('.game-card-img').each(function() {
    let id = $(this).attr('id') + '-clickable';
    let offset = $(this).offset();
    let height = $(this).height();
    let width = numClickableDivsCreated === numCards - 1 ? $(this).width() : overlappedClickableCardWidth;
    let styles = [
      'position:absolute',
      'top:' + offset.top,
      'left:' + offset.left,
      'height:' + height,
      'width:' + width,
    ];
    $('body').append(
      '<div class="clickable-game-card" id="' +
      id +
      '" style="' +
      styles.join(';') +
      '"></div>'
    );
    numClickableDivsCreated++;
  });
  $('.clickable-game-card').click(function() {
    let id = $(this).attr('id');
    let i = id.indexOf('-clickable');
    let cardId = id.substring(0, i);
    $(this).toggleClass('clickable-card-selected');
    $('#' + cardId).parent().toggleClass('card-selected');
    updateButtons();
  });
}

function resizeCenter() {
  let gameCenterTop = $('#game-center').offset().top;
  let gameHandTop = $('#game-hand-wrapper').offset().top;
  let numPxInRem = parseFloat(getComputedStyle(document.documentElement).fontSize);
  $('#game-center').css('height', Math.min(200, gameHandTop - gameCenterTop - 2 * numPxInRem) + 'px');
}

function getSelectedCards() {
  let hand = [];
  $('.game-card.card-selected').each(function() {
    let valueAndSuit = $(this).children('img').attr('id').split('-');
    let value = parseInt(valueAndSuit[0]);
    if (value == 14 || value == 15) {
      hand.push({ value: value });
    } else {
      hand.push({
        value: value,
        suit: parseInt(valueAndSuit[1]),
      });
    }
  });
  return hand;
}

function getUid() {
  let hand = getSelectedCards();
  let cookies = document.cookie.split(';');
  for (c of cookies) {
    let keyAndValue = c.trim().split('=');
    if (keyAndValue[0] == 'uid') {
      return parseInt(keyAndValue[1]);
    }
  }
  return null;
}

function updateButtons() {
  let hand = getSelectedCards();
  let uid = getUid();
  if (uid == null) {
    location.reload();
  } else {
    socket.emit('check', uid, hand.length === 0 ? null : hand);
  }
}

setCardImages();
overlapCardsAndCreateClickableDivs();
resizeCenter();
updateButtons();

window.addEventListener('resize', function() {
  overlapCardsAndCreateClickableDivs();
  resizeCenter();
});

$('#play').click(function() {
  let hand = getSelectedCards();
  let uid = getUid();
  if (uid == null) {
    location.reload();
  } else {
    socket.emit('play', uid, hand);
  }
});

$('#pass').click(function() {
  let uid = getUid();
  if (uid == null) {
    location.reload();
  } else {
    socket.emit('play', uid, null);
  }
});

socket.on('num spectators', (data) => {
  $('#spectators').text('Spectators: ' + data.numSpectators);
});

socket.on('check ok', (data) => {
  if (data.pass) {
    $('#play').prop('disabled', true);
    $('#play').prop('title', 'Select cards to play');
  } else {
    $('#play').prop('disabled', false);
    $('#play').removeProp('title');
  }
  $('#pass').prop('disabled', false);
  $('#pass').removeProp('title');
});

socket.on('check error', (data) => {
  $('#play').prop('disabled', true);
  $('#play').prop('title', data.err);
  if (data.err == 'Not your turn') {
    $('#pass').prop('disabled', true);
    $('#pass').prop('title', data.err);
  } else {
    $('#pass').prop('disabled', false);
    $('#pass').removeProp('title');
  }
});

socket.on('game error', (data) => {
  $('body').html(
    '<div class="center" style="text-align:center">' +
    '<h1>Error</h1>' +
    '<br>' +
    data.err +
    '</div>'
  );
});

socket.on('play update', (data) => {
  if (data.getMetadataUpdate) {
    let uid = getUid();
    if (uid == null) {
      location.reload();
    } else {
      socket.emit('get metadata update', uid);
    }
  }
  $('#game-center').html(data.gameCenter);
  // Hand HTML is only present for the person who just played.
  if (data.hasOwnProperty('gameHand')) {
    $('#game-hand').html(data.gameHand);
    setCardImages();
    overlapCardsAndCreateClickableDivs();
  } else {
    setCardImages();
  }
  resizeCenter();
  updateButtons();
});

socket.on('metadata update', (data) => {
  $('#title').text(data.title);
  if (data.hasOwnProperty('gamePlayers')) {
    $('#game-players').html(data.gamePlayers);
    resizeCenter();
  }
});
